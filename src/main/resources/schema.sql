CREATE TABLE IF NOT EXISTS mpa_ratings (
	id TINYINT GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
	name VARCHAR(10) NOT NULL
);

CREATE TABLE IF NOT EXISTS genres (
	id SMALLINT GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
	name VARCHAR(50) NOT NULL
);

CREATE TABLE IF NOT EXISTS films (
	id INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
	name VARCHAR(255) NOT NULL,
	description VARCHAR,
	release_date DATE,
	duration SMALLINT,
	mpa_rating_id TINYINT REFERENCES mpa_ratings(id)
);

CREATE TABLE IF NOT EXISTS users (
	id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
	email VARCHAR(100) NOT NULL,
	login VARCHAR(100) NOT NULL,
	name VARCHAR(255),
	birthday DATE
);

CREATE TABLE IF NOT EXISTS film_genres (
	film_id INTEGER NOT NULL PRIMARY KEY REFERENCES films(id),
	genre_id SMALLINT NOT NULL REFERENCES genres(id)
);

CREATE TABLE IF NOT EXISTS film_likes (
	film_id INTEGER NOT NULL PRIMARY KEY REFERENCES films(id),
	user_id BIGINT NOT NULL REFERENCES users(id)
);

CREATE TABLE IF NOT EXISTS user_followees (
	user_id BIGINT NOT NULL PRIMARY KEY REFERENCES users(id),
	followee_id BIGINT NOT NULL REFERENCES users(id)
);

CREATE TABLE IF NOT EXISTS user_friends (
	user_id BIGINT NOT NULL PRIMARY KEY REFERENCES users(id),
	friend_id BIGINT NOT NULL REFERENCES users(id)
);