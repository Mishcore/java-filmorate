DROP TABLE IF EXISTS mpa_ratings, genres, films, users, film_genres, film_likes, user_followees, user_friends;

CREATE TABLE mpa_ratings (
	id TINYINT GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
	name VARCHAR(10) NOT NULL UNIQUE
);

CREATE TABLE genres (
	id SMALLINT GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
	name VARCHAR(50) NOT NULL UNIQUE
);

CREATE TABLE films (
	id INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
	name VARCHAR(255) NOT NULL UNIQUE,
	description VARCHAR UNIQUE,
	release_date DATE,
	duration SMALLINT,
	mpa_rating_id TINYINT REFERENCES mpa_ratings(id)
);

CREATE TABLE users (
	id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
	email VARCHAR(100) NOT NULL UNIQUE,
	login VARCHAR(100) NOT NULL UNIQUE,
	name VARCHAR(255),
	birthday DATE
);

CREATE TABLE film_genres (
	film_id INTEGER NOT NULL REFERENCES films(id) ON DELETE CASCADE,
	genre_id SMALLINT NOT NULL REFERENCES genres(id) ON DELETE CASCADE
);

CREATE TABLE film_likes (
	film_id INTEGER NOT NULL REFERENCES films(id) ON DELETE CASCADE,
	user_id BIGINT NOT NULL REFERENCES users(id) ON DELETE CASCADE
);

CREATE TABLE user_followees (
	user_id BIGINT NOT NULL REFERENCES users(id) ON DELETE CASCADE,
	followee_id BIGINT NOT NULL REFERENCES users(id) ON DELETE CASCADE
);

CREATE TABLE user_friends (
	user_id BIGINT NOT NULL REFERENCES users(id) ON DELETE CASCADE,
	friend_id BIGINT NOT NULL REFERENCES users(id) ON DELETE CASCADE
);